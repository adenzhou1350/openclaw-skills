# 主函数
main() {
    local command="${1:-help}"
    shift || true
    
    # 解析全局选项
    while [[ $# -gt 0 ]]; do
        case "$1" in
            --title|-T)
                REPORT_TITLE="$2"
                shift 2
                ;;
            --output|-o)
                OUTPUT_FILE="$2"
                shift 2
                ;;
            --format|-f)
                OUTPUT_FORMAT="$2"
                shift 2
                ;;
            --service|-s)
                SERVICE_NAME="$2"
                shift 2
                ;;
            --status)
                SERVICE_STATUS="$2"
                shift 2
                ;;
            --severity)
                VAR_severity="$2"
                shift 2
                ;;
            --week)
                VAR_week="$2"
                shift 2
                ;;
            --month)
                VAR_month="$2"
                shift 2
                ;;
            --year)
                VAR_year="$2"
                shift 2
                ;;
            --vars)
                parse_vars "$2"
                shift 2
                ;;
            --type|-t)
                REPORT_TYPE="$2"
                shift 2
                ;;
            --schedule)
                CRON_SCHEDULE="$2"
                shift 2
                ;;
            -h|--help)
                show_help
                exit 0
                ;;
            *)
                log_error "未知选项: $1"
                show_help
                exit 1
                ;;
        esac
    done
    
    # 执行命令
    case "$command" in
        generate|gen)
            case "$REPORT_TYPE" in
                project) generate_project_report ;;
                weekly) generate_weekly_report ;;
                monthly) generate_monthly_report ;;
                service) generate_service_report ;;
                alert) generate_alert_report ;;
                *) generate_project_report ;;
            esac
            ;;
        weekly|w)
            generate_weekly_report
            ;;
        monthly|m)
            generate_monthly_report
            ;;
        service|s)
            generate_service_report
            ;;
        project|p)
            generate_project_report
            ;;
        template|t)
            case "$REPORT_TYPE" in
                monthly) generate_monthly_report ;;
                weekly) generate_weekly_report ;;
                service) generate_service_report ;;
                project) generate_project_report ;;
                alert) generate_alert_report ;;
                *) generate_project_report ;;
            esac
            ;;
        list-templates|lt)
            list_templates
            exit 0
            ;;
        alert|a)
            generate_alert_report
            ;;
        cron)
            if [[ -z "$CRON_SCHEDULE" ]]; then
                log_error "请指定 --schedule 参数"
                exit 1
            fi
            log_info "创建定时任务: $CRON_SCHEDULE"
            echo "# 定时报告任务
# 添加以下行到 crontab:
# $CRON_SCHEDULE $SCRIPT_DIR/\$(basename \$0) weekly -o ~/reports/weekly-\$(date +\%Y-\%W).md"
            ;;
        help|h)
            show_help
            exit 0
            ;;
        *)
            log_error "未知命令: $command"
            show_help
            exit 1
            ;;
    esac
    
    # 输出到文件
    if [[ -n "$OUTPUT_FILE" ]]; then
        if [[ -d "$OUTPUT_FILE" ]]; then
            OUTPUT_FILE="$OUTPUT_DIR/$(basename "${REPORT_TITLE:-report}" 2>/dev/null || echo "report").md"
        fi
        log_success "报告已保存到: $OUTPUT_FILE"
    fi
}

# 如果没有参数，显示帮助
if [[ $# -eq 0 ]]; then
    show_help
    exit 0
fi

main "$@"
