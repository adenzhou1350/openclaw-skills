#!/bin/bash
#
# Service Report Generator - 自动化服务报告生成工具
# 
# 使用方法:
#   ./service-report.sh generate --type project --title "Title" --output file.md
#   ./service-report.sh weekly --week 2026-W10 --output weekly.md
#   ./service-report.sh service --service "API" --status healthy --output service.md
#   ./service-report.sh template --name monthly --vars "month=March,year=2026"
#   ./service-report.sh list-templates
#   ./service-report.sh cron --schedule "cron" --type weekly
#

set -e

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
TEMPLATE_DIR="$SCRIPT_DIR/templates"
OUTPUT_DIR="${OUTPUT_DIR:-$SCRIPT_DIR/output}"

# 颜色定义
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# 日志函数
log_info() { echo -e "${BLUE}[INFO]${NC} $1"; }
log_success() { echo -e "${GREEN}[SUCCESS]${NC} $1"; }
log_warn() { echo -e "${YELLOW}[WARN]${NC} $1"; }
log_error() { echo -e "${RED}[ERROR]${NC} $1"; }

# 创建必要目录
mkdir -p "$TEMPLATE_DIR" "$OUTPUT_DIR"

# 默认值
OUTPUT_FORMAT="markdown"
REPORT_TITLE=""
REPORT_DATE=$(date +"%Y-%m-%d %H:%M:%S")
REPORT_TYPE="general"

# 帮助信息
