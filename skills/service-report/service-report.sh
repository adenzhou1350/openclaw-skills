# 解析变量字符串
parse_vars() {
    local vars="$1"
    IFS=',' read -ra VAR_ARRAY <<< "$vars"
    for var in "${VAR_ARRAY[@]}"; do
        key=$(echo "$var" | cut -d'=' -f1)
        value=$(echo "$var" | cut -d'=' -f2-)
        export "VAR_$key=$value"
    done
}

# 替换模板变量
replace_vars() {
    local content="$1"
    content="${content//\{\{title\}\}/$REPORT_TITLE}"
    content="${content//\{\{date\}\}/$REPORT_DATE}"
    content="${content//\{\{service\}\}/$SERVICE_NAME}"
    content="${content//\{\{status\}\}/$SERVICE_STATUS}"
    content="${content//\{\{month\}\}/${VAR_month:-$(date +%B)}}"
    content="${content//\{\{year\}\}/${VAR_year:-$(date +%Y)}}"
    content="${content//\{\{week\}\}/${VAR_week:-$(date +%V)}}"
    echo "$content"
}

